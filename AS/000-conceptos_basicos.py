# VARIABLES: Se utilizan para almacenar valores de cualquier tipo de dato, para poder ser utilizados en el futuro.
# 1. Dar(asignar) valor a variables
edad = 18
# edad (es el nombre de la variable)
#  =  (asigna el valor)
# 18  (es el valor de la variable)

sueldo=100.55 # A la variable sueldo le asignamos el valor 100.55
nombre="juan perez" # A la variable nombre le asignamos el valor "juan perez"
casado=True # A la variable casado le asignamos el valor True (verdadero)


# 2. IMPORTANTE: Las varibles no puede empezar con números o símbolos (caracteres especiales: $, %, &, @, +, / etc)
# Ejemplo de variables incorrectas:
# 1edad=18 # Incorrecto
# $sueldo=100.55 # Incorrecto
