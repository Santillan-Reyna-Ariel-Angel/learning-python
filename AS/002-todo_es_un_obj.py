# CUALQUIER COSA(variables, funciones, etc) EN PYTHON ES UN OBJETO:
edad=60 # edad es una VARIABLE(objeto) de tipo entero
print(type(edad)) # type() nos da el tipo de dato de la VARIABLE

nombre="Juan" # nombre es una VARIABLE(objeto) de tipo string o cadena (todo lo que se encierre entre comillas dobles o simples es un string)
print(type(nombre)) # type() nos da el tipo de dato de la VARIABLE

print(id(edad)) # id() nos da el id de la VARIABLE
print(repr(edad)) # repr() nos da el valor de la VARIABLE

