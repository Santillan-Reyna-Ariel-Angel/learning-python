# IMPRIMIR DATOS EN CONSOLA:
# 1. print() es una función que imprime en consola lo que tenga dentro de los paréntesis (imprime el valor de las variables)

print(8) # imprime un número entero
print(28.5) # imprime un número flotante (números con decimales)
print("Hola mundo")  # imprime una cadena o string (letras/palabras)
print('a')  # imprime una cadena o string (letras/palabras)
print(True)
print(False)

# 2. imprimir el valor de una variable
edad=18
print(edad)

sueldo=100.55
print(sueldo)

nombre="juan perez"
print(nombre)

casado=True
print(casado)


# 3. Imprimir dos o mas valores en un solo print
# Para imprimir dos o mas valores en un solo print se utiliza una coma (,) para separar los valores:
print(nombre, casado)
print(nombre, casado, edad, sueldo)

# Tambien podemos imprimir texto y variables en un solo print para que se vea mas entendible:
print("Nombre:", nombre)
print("Casado:", casado)
print("Edad:", edad)
print("Sueldo:", sueldo)

print("Nombre:", nombre, "Casado:", casado, "Edad:", edad, "Sueldo:", sueldo)